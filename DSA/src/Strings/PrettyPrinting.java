package Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 365.5434f;
        System.out.printf("Formatted float is %.2f",a);     //formatted output
        System.out.println();
        System.out.printf("Pie : %.3f",Math.PI);
        System.out.println();
        System.out.printf("hello my name is %s and my surname is %s","Shivam","Jha");   //%d %s are placeholders


    }

}
